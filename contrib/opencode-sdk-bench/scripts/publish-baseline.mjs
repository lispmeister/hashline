#!/usr/bin/env node
import fs from "node:fs"
import path from "node:path"

const runsDir = path.resolve("runs")
const out = []

for (const file of fs.readdirSync(runsDir).filter((f) => f.endsWith(".json") && f !== "progress.json")) {
  const report = JSON.parse(fs.readFileSync(path.join(runsDir, file), "utf8"))
  let attempts = 0
  let pass = 0
  let task = 0
  let protocol = 0
  let corrupt = 0
  let cost = 0
  for (const c of report.results || []) {
    for (const a of c.attempts || []) {
      attempts += 1
      pass += a.overallPassed ? 1 : 0
      task += a.taskPassed ? 1 : 0
      protocol += a.protocolPassed ? 1 : 0
      corrupt += a.corruptionDetected ? 1 : 0
      cost += a.metrics?.cost || 0
    }
  }
  out.push({
    file,
    mode: report.config?.mode,
    model: report.model?.label,
    attempts,
    overallPassRate: +(100 * pass / Math.max(1, attempts)).toFixed(1),
    taskPassRate: +(100 * task / Math.max(1, attempts)).toFixed(1),
    protocolPassRate: +(100 * protocol / Math.max(1, attempts)).toFixed(1),
    corruptionRate: +(100 * corrupt / Math.max(1, attempts)).toFixed(1),
    totalCost: +cost.toFixed(6),
  })
}

out.sort((a, b) => a.model.localeCompare(b.model) || a.mode.localeCompare(b.mode))
const date = new Date().toISOString().slice(0, 10)
const md = [
  `# Baseline Snapshot (${date})`,
  "",
  "| Model | Mode | Attempts | Overall | Task | Protocol | Corruption | Cost | File |",
  "|---|---:|---:|---:|---:|---:|---:|---:|---|",
  ...out.map((r) => `| ${r.model} | ${r.mode} | ${r.attempts} | ${r.overallPassRate}% | ${r.taskPassRate}% | ${r.protocolPassRate}% | ${r.corruptionRate}% | ${r.totalCost} | ${r.file} |`),
  "",
  "Generated by `npm run baseline:publish`."
].join("\n")

const target = path.resolve(`BASELINE_${date}.md`)
fs.writeFileSync(target, `${md}\n`, "utf8")
console.log(target)
